def find_number(length,target,string_two):
    label = 0
    if target in string_two:
        label = 1

    if label == 1:
        for index in range(len(string_two)):
            if string_two[index] == target:
                target_index = index
                if index < (length-1)//2:
                    continue
                elif index > (length-1)//2:
                    break
                else:
                    return 0
                break
        median_index = (length-1)//2
        # print(target_index)
        # print(string_two[target_index],string_two[target_index+1])
        if target_index >= median_index:
            number = 2 * target_index - length + 1   #按奇数length算最短!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
        else:
            number = length - 2 * target_index - 2   #按偶数length算最短!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
        return number
    else:
        length = length + 1
        for index in range(len(string_two)):
            if string_two[index] < target:
                insert_index = index + 1
            else:
                break
        if insert_index == (length - 1) // 2:
            return 1
        string_two.insert(insert_index,target)
        median_index = (length-1)//2
        if insert_index >= median_index:
            number = 2 * insert_index - length + 1
        else:
            number = length - 2 * insert_index -2
        return number + 1


